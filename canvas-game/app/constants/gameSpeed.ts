/** ====== 스폰 속도 관련 설정 ====== */

/** 처음 스폰 간격 (ms) */
export const BASE_SPAWN_MS = 2600;

/** 레벨 1 오를 때마다 줄어드는 시간 (ms) */
export const SPAWN_DECREASE_PER_LEVEL = 150;

/** (선택) 너무 빨라지는 것 방지용 최소 스폰 간격 (ms) */
export const MIN_SPAWN_MS = 600;

/** 레벨 업에 필요한 점수 단위 (UI / 표시용) */
export const STEP_LEVEL = 60;


/** ====== 함수 ====== */

export function calcLevel(score: number): number {
  return 1 + Math.floor(score / STEP_LEVEL);
}

export function calcSpawnInterval(level: number): number {
  const ms = BASE_SPAWN_MS - (level - 1) * SPAWN_DECREASE_PER_LEVEL;
  return Math.max(MIN_SPAWN_MS, ms);

}

/**
 * ================== 게임 속도 / 난이도 조정 가이드 ==================
 *
 * 이 파일은 "시간(스폰 속도)과 레벨 가속"을 한 곳에서 관리하기 위한 설정 파일이다.
 * 게임 로직(App.tsx)은 이 파일의 값만 참조하고, 직접 숫자를 계산하지 않는다.
 *
 * ▶ 기본 개념
 * - score(점수) → level(레벨) → spawnInterval(타일 생성 속도)
 * - 점수가 올라갈수록 level이 자동 증가
 * - level이 올라갈수록 spawnInterval(ms)이 줄어들어 게임이 빨라짐
 *
 * ------------------------------------------------------------------
 * [BASE_SPAWN_MS]
 * - 레벨 1일 때의 기본 스폰 간격 (밀리초)
 * - 값이 클수록 초반 게임이 느려짐
 * - 예:
 *   3000 = 매우 느린 시작
 *   2400 = 보통
 *   1800 = 빠른 시작
 *
 * ------------------------------------------------------------------
 * [LEVEL_START_SCORE]
 * - 이 점수부터 레벨업이 시작됨
 * - 이 값 미만에서는 항상 level = 1
 * - 예:
 *   50  = 초반부터 빠르게 난이도 상승
 *   100 = 초반 완만
 *   200 = 튜토리얼 느낌
 *
 * ------------------------------------------------------------------
 * [SPAWN_DECREASE_PER_LEVEL]
 * - 레벨이 1 오를 때마다 줄어드는 스폰 간격(ms)
 * - 값이 클수록 난이도 상승이 가파름
 * - 예:
 *   80  = 완만한 가속
 *   150 = 기본 권장
 *   250 = 후반 급격히 어려움
 *
 * ------------------------------------------------------------------
 * [MIN_SPAWN_MS]
 * - 아무리 레벨이 올라가도 이 값보다 빨라지지 않음 (하한선)
 * - 너무 낮으면 사람이 반응 불가
 * - 예:
 *   800 = 비교적 여유 있음
 *   600 = 권장 최소
 *   400 = 하드코어
 *
 * ------------------------------------------------------------------
 * [calcLevel(score)]
 * - 점수를 기반으로 현재 레벨을 계산하는 함수
 * - level을 state로 관리하지 않고, 항상 score에서 계산함
 * - 공식:
 *   level = 1 + floor((score - LEVEL_START_SCORE) / LEVEL_START_SCORE)
 *
 * ------------------------------------------------------------------
 * [calcSpawnInterval(level)]
 * - 레벨을 기반으로 실제 스폰 속도(ms)를 계산
 * - BASE_SPAWN_MS에서 레벨당 감소값을 빼고
 * - MIN_SPAWN_MS 이하로는 내려가지 않게 clamp
 *
 * ------------------------------------------------------------------
 * ▶ 튜닝 팁
 * - "초반 너무 빡셈" → BASE_SPAWN_MS ↑ 또는 LEVEL_START_SCORE ↑
 * - "후반이 심심함" → SPAWN_DECREASE_PER_LEVEL ↑ 또는 MIN_SPAWN_MS ↓
 * - "전체적으로 템포 빠름" → BASE_SPAWN_MS ↑
 *
 * 이 파일만 수정하면 게임 밸런스를 전부 조절할 수 있어야 정상이다.
 * ================================================================
 */
